# listi sortlamak

months = ['yanwar', 'fewral', 'mart', 'aprel', 'may']

# bu funksiýa listleri alfabetik yzygiderlikde sortlar
months.sort()
print(months)

# bu funksiýa listleri alfabetik yzygiderligiň tersine sortlar
months.sort(reverse=True)
print(months)


# listi öňki yzygiderligine getireliň
months = ['yanwar', 'fewral', 'mart', 'aprel', 'may']

print(months)

# bu funksiýa listi tersine print eder
months.reverse()
