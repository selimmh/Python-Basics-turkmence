# bu faýl listleri nädip üýtgetmelidigini öz içine alar

my_friends = ['merdan', 'perman', 'rustem']

# listi printlemek
print(my_friends)

# listiň elementini üýtgetmek
my_friends[0] = 'agosh'
print(my_friends)

# listi giňeltmek
my_friends.append('shohrat')
print(my_friends)

# listi boşatmak
my_friends = []
print(my_friends)

# liste täze element goşmak
my_friends.append('perman')
my_friends.append('selim')
print(my_friends)

# elementi belli bir pozisiýa ýerleşdirmek
my_friends.insert(1, 'agosh')
print(my_friends)

# listden belli bir elementi pozmak
del my_friends[2]
print(my_friends)