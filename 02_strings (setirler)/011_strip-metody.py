#'strip' metody setirdäki ähli boş ýerleri pozýar

address = "     18 Bitaraplyk Şaýoly     "
print(address.strip())


#'rstrip' metody diňe sag tarapdaky boş ýerleri pozýar
#'lstrip' metody diňe çep tarapdaky boş ýerleri pozýar

address = "18 Bitaraplyk Şaýoly     "
print(address.rstrip())

address = "     18 Bitaraplyk Şaýoly"
print(address.lstrip())